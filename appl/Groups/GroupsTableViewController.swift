    @IBAction func addCity(segue: UIStoryboardSegue) {
        // Проверяем идентификатор, чтобы убедится, что это нужный переход
        if segue.identifier == "addGroups" {
            // Получаем ссылку на контроллер, с которого осуществлен переход
            let allCitiesController = segue.source as! AllGroupsTableViewController
            
            // Получаем индекс выделенной ячейки
            if let indexPath = allCitiesController.tableView.indexPathForSelectedRow {
                // Получаем город по индексу
                let oneGroup = allCitiesController.allGroups[indexPath.row]
                // Проверяем, что такого города нет в списке
                if !groups.contains(oneGroup) {
                    // Добавляем город в список выбранных
                    groups.append(oneGroup)

                // Обновляем таблицу
                tableView.reloadData()
            }
            }
            
        }

    }

 

